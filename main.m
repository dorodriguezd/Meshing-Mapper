%% DAT optimization examples: serial, parallel, and optional MEX
% Disabled by default. Set runOptimizationExamples=true before running
% main.m. These cases use the same sphere/baseline geometry and write
% separate outputs so their optimizationInfo structures can be compared.
if runOptimizationExamples
    serialSetup = sphereSetup;
    serialSetup.NewLabels = 41;
    serialSetup.NewLabelNames = "serial sphere";
    serialSetup.OutputFile = fullfile(exampleOutput, ...
        'dat_optimization_serial_matlab.dat');
    serialSetup.LogFile = fullfile(exampleOutput, ...
        'dat_optimization_serial_matlab_log.txt');
    serialSetup.UseParallel = false;
    serialSetup.UseMex = false;
    serial_result = remap(baseline, sphereSource, serialSetup);

    parallelSetup = sphereSetup;
    parallelSetup.NewLabels = 42;
    parallelSetup.NewLabelNames = "parallel sphere";
    parallelSetup.OutputFile = fullfile(exampleOutput, ...
        'dat_optimization_parallel_matlab.dat');
    parallelSetup.LogFile = fullfile(exampleOutput, ...
        'dat_optimization_parallel_matlab_log.txt');
    parallelSetup.UseParallel = true;
    parallelSetup.ParallelPoolType = "auto";
    parallelSetup.ParallelWorkers = 32;
    parallelSetup.ChunkSize = 500;
    parallelSetup.UseMex = false;
    parallel_result = remap(baseline, sphereSource, parallelSetup);

    mexSetup = sphereSetup;
    mexSetup.NewLabels = 43;
    mexSetup.NewLabelNames = "MEX-enabled sphere";
    mexSetup.OutputFile = fullfile(exampleOutput, ...
        'dat_optimization_mex.dat');
    mexSetup.LogFile = fullfile(exampleOutput, ...
        'dat_optimization_mex_log.txt');
    mexSetup.UseParallel = false;
    mexSetup.UseMex = true;
    mexSetup.BuildMex = false;
    mexSetup.MexRequired = false;
    mex_result = remap(baseline, sphereSource, mexSetup);

    if runMexBuildExample
        mexBuildSetup = mexSetup;
        mexBuildSetup.NewLabels = 44;
        mexBuildSetup.NewLabelNames = "compiled MEX sphere";
        mexBuildSetup.OutputFile = fullfile(exampleOutput, ...
            'dat_optimization_build_mex.dat');
        mexBuildSetup.LogFile = fullfile(exampleOutput, ...
            'dat_optimization_build_mex_log.txt');
        mexBuildSetup.BuildMex = true;
        mexBuildSetup.MexVerbose = false;
        mexBuildSetup.MexRequired = true;
        mex_build_result = remap(baseline, sphereSource, mexBuildSetup);
    end
end

fprintf('\nAll examples completed. Outputs: %s\n', exampleOutput);
