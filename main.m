%% Meshing-Mapper release examples
% This script is the main user workspace. Each section defines a baseline,
% a labeled source, the labels to transfer, and the baseline labels where
% replacement is allowed. Mesh dimensions are expressed in centimeters.

close all;
clc;

repoRoot = fileparts(mfilename('fullpath'));
addpath(fullfile(repoRoot, 'Lib'));

exampleData = fullfile(repoRoot, 'examples', 'mapper', 'data');
exampleOutput = fullfile(repoRoot, 'examples', 'mapper', 'output');
if ~exist('figureVisibility', 'var')
    figureVisibility = 'on';
end

% Choose how the example inputs are obtained:
%   "load"     use the committed DAT/MSH files (fast, default)
%   "generate" recreate every DAT/MSH file from the geometry definitions
if ~exist('exampleDataMode', 'var')
    exampleDataMode = "load";
end
if ~exist('runOptimizationExamples', 'var')
    runOptimizationExamples = false;
end
if ~exist('runMexBuildExample', 'var')
    runMexBuildExample = false;
end
files = resolveExampleMeshes(exampleData, exampleDataMode);

%% DAT example 1: centered 3 cm sphere in a homogeneous 5 cm cube
baseline = files.BaselineDat;
sphereSource = files.SphereDat;

visualizeDatLabelGroups(baseline, {10}, ...
    'GroupNames', "homogeneous baseline", ...
    'Colors', [0.70 0.74 0.80], ...
    'Alphas', 0.22, ...
    'Visible', figureVisibility, ...
    'OutputFolder', exampleOutput, ...
    'OutputName', 'dat_1_starting_baseline', ...
    'Title', 'DAT example 1 - starting 5 cm baseline');
visualizeDatLabelGroups(sphereSource, {1}, ...
    'GroupNames', "3 cm sphere", ...
    'Colors', [0.90 0.25 0.20], ...
    'Alphas', 0.90, ...
    'Visible', figureVisibility, ...
    'OutputFolder', exampleOutput, ...
    'OutputName', 'dat_1_source_sphere', ...
    'Title', 'DAT example 1 - source sphere');

sphereSetup = struct();
sphereSetup.InputLabels = 1;
sphereSetup.NewLabels = 11;
sphereSetup.TargetLabels = 10;
sphereSetup.InputLabelNames = "sphere";
sphereSetup.NewLabelNames = "mapped_sphere";
sphereSetup.OutputFile = fullfile(exampleOutput, 'dat_1_sphere_remap.dat');
sphereSetup.LogFile = fullfile(exampleOutput, 'dat_1_sphere_remap_log.txt');
sphere_remap = remap(baseline, sphereSource, sphereSetup);

visualizeDatLabelGroups(sphere_remap.OutputFile, {10, 11}, ...
    'GroupNames', ["baseline", "mapped sphere"], ...
    'Colors', [0.70 0.74 0.80; 0.90 0.25 0.20], ...
    'Alphas', [0.16 0.90], ...
    'Visible', figureVisibility, ...
    'OutputFolder', exampleOutput, ...
    'OutputName', 'dat_1_remapped', ...
    'Title', 'DAT example 1 - remapped baseline');

%% DAT example 2: map all three source labels into both baseline layers
layeredBaseline = files.LayeredBaselineDat;
multiShapeSource = files.MultiShapeDat;

visualizeDatLabelGroups(layeredBaseline, {10, 20}, ...
    'GroupNames', ["outer 5 cm cube", "denser inner 4 cm cube"], ...
    'Colors', [0.65 0.70 0.78; 0.30 0.55 0.85], ...
    'Alphas', [0.12 0.25], ...
    'Visible', figureVisibility, ...
    'OutputFolder', exampleOutput, ...
    'OutputName', 'dat_2_starting_layered_baseline', ...
    'Title', 'DAT example 2 - two-layer baseline');
visualizeDatLabelGroups(multiShapeSource, {1, 2, 3}, ...
    'GroupNames', ["3 cm sphere", "1 cm sphere", "0.5 cm cube"], ...
    'Colors', [0.78 0.82 0.90; 0.15 0.65 0.30; 0.95 0.55 0.10], ...
    'Alphas', [0.20 0.95 0.95], ...
    'Visible', figureVisibility, ...
    'OutputFolder', exampleOutput, ...
    'OutputName', 'dat_2_source_multishape', ...
    'Title', 'DAT example 2 - three-label source');

allLabelsSetup = struct();
allLabelsSetup.InputLabels = [1 2 3];
allLabelsSetup.NewLabels = [31 32 33];
allLabelsSetup.TargetLabels = [10 20];
allLabelsSetup.InputLabelNames = ["large sphere", "small sphere", "small cube"];
allLabelsSetup.NewLabelNames = ["mapped large sphere", "mapped small sphere", ...
    "mapped small cube"];
allLabelsSetup.OutputFile = fullfile(exampleOutput, 'dat_2_all_labels_remap.dat');
allLabelsSetup.LogFile = fullfile(exampleOutput, 'dat_2_all_labels_remap_log.txt');
all_labels_remap = remap(layeredBaseline, multiShapeSource, allLabelsSetup);

visualizeDatLabelGroups(all_labels_remap.OutputFile, {10, 20, 31, 32, 33}, ...
    'GroupNames', ["outer baseline", "inner baseline", "large sphere", ...
    "small sphere", "small cube"], ...
    'Colors', [0.65 0.70 0.78; 0.30 0.55 0.85; 0.78 0.82 0.90; ...
    0.15 0.65 0.30; 0.95 0.55 0.10], ...
    'Alphas', [0.08 0.12 0.18 0.95 0.95], ...
    'Visible', figureVisibility, ...
    'OutputFolder', exampleOutput, ...
    'OutputName', 'dat_2_all_labels_remapped', ...
    'Title', 'DAT example 2 - all source labels mapped');

